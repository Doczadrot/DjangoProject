    # max_length=6 - –º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞ (—Å –∑–∞–ø–∞—Å–æ–º –¥–ª—è –≤–æ–∑–º–æ–∂–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π)
    # choices=YEAR_IN_SCHOOL_CHOICE - —É–∫–∞–∑—ã–≤–∞–µ—Ç, —á—Ç–æ –∑–Ω–∞—á–µ–Ω–∏—è –¥–ª—è —ç—Ç–æ–≥–æ –ø–æ–ª—è –¥–æ–ª–∂–Ω—ã –≤—ã–±–∏—Ä–∞—Ç—å—Å—è –∏–∑ —Å–ø–∏—Å–∫–∞ YEAR_IN_SCHOOL_CHOICE
